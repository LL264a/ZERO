def fn4(a):
	# 在函数中对形参进行重新赋值，不会影响其他的变量
	# a = 20
	# a 是一个列表，尝试修改列表中的元素
	# 如果形参执行是一个对象，当我们通过形参去修改对象是
	# 	会影响到所有只想该对象的变量
	a[0] = 30
	print('a = ',a,id(a))

